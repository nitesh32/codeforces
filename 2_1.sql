    EMPNO  NUMERIC(4) NOT NULL,
    ENAME VARCHAR(10),
    JOB VARCHAR(19),
    MGR NUMERIC(4),
    HIREDATE DATE,
    SAL NUMERIC(7,2),
    COMM NUMERIC(7,2),
    DEPTNO NUMERIC(3),
    AGE NUMERIC(3),
    ESAL NUMERIC
);

