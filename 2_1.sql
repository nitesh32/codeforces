select * from EMP;
select *,MAX(SAL) AS "MAX SALARY" FROM EMP WHERE SAL = (SELECT MAX(SAL) FROM EMP);
select *,MIN(SAL) AS "MIN SALARY" FROM EMP WHERE SAL = (SELECT MIN(SAL) FROM EMP) ;
select AVG(SAL) AS "AVERAGE SALARY" FROM EMP ;

SELECT MAX(SAL) AS "MAX SAL",MIN(SAL) AS "MIN SAL",MAX(SAL)-MIN(SAL) AS "DIFFERENCE" FROM EMP;
SELECT ENAME,SAL,JOB FROM EMP WHERE SAL>(SELECT MIN(SAL) FROM EMP) AND JOB LIKE "M%";

SELECT JOB AS "JOB",COUNT(*) AS "FREQUENCY",SUM(SAL) AS "SAL SPENT FOR EACH JOB CATAGORY" FROM EMP GROUP BY JOB;

SELECT MGR "MANAGER" ,COUNT(*) AS "NUMBER OF EMPLOYES" ,MIN(SAL) AS "LOWEST PAID EMPLOYEE"FROM EMP GROUP BY MGR;